package com.example.appdubaothoitiet;

public class Weather {
    public String Ngay;
    public String Trangthai;
    public String Hinhanh;
    public String MaxTemp;
    public String MinTemp;

    public Weather(String ngay, String trangthai, String hinhanh, String maxTemp, String minTemp) {
        Ngay = ngay;
        Trangthai = trangthai;
        Hinhanh = hinhanh;
        MaxTemp = maxTemp;
        MinTemp = minTemp;
    }
}
